//Write a java program to input two numbers and find the maximum number using switch case.

package com.java;

public class Switch {
	public static void main(String args[])
	{
		
		int a=1;
		switch(a) {
		
		case 1:
			System.out.println("one");
			break;
		case 2:
			System.out.println("two");
			break;
		case 3:
			System.out.println("three");
			break;
		default:
			System.out.println("invalid");
			break;
			
			
		
		} 
		
		
	}
}


